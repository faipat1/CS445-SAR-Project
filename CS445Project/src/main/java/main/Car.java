package main;

public class Car {

	private String make;
	private String model;
	private String color;
	private String plate_state;
	private String plate_serial;
	
	public Car(String make, String model, String color, String plateState, String plateSerial) {
		this.make = make;
		this.model = model;
		this.color = color;
		this.plate_state = plateState;
		this.plate_serial = plateSerial;
	}
	
}

